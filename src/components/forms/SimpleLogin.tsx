'use client';

import { useState, useRef } from 'react';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { cn } from '@/lib/utils';
import Link from 'next/link';
import { motion, AnimatePresence } from 'framer-motion';

// ============================================================================
// ICONOS
// ============================================================================

import {
  Eye,
  EyeOff,
  LogIn,
  Mail,
  Lock,
  AlertCircle,
  CheckCircle2,
  ArrowRight,
  Shield,
  Sparkles,
  Store,
  Clock,
  Leaf,
  User,
  Check
} from 'lucide-react';

// ============================================================================
// COMPONENTE PRINCIPAL - VERSIÓN INICIAL FUNCIONAL
// ============================================================================

export function SimpleLogin() {
  const [showPassword, setShowPassword] = useState(false);
  const [isLoading, setIsLoading] = useState(false);
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [errors, setErrors] = useState<Record<string, string>>({});
  const [rememberMe, setRememberMe] = useState(false);

  const validateForm = (): boolean => {
    const newErrors: Record<string, string> = {};

    if (!email.trim()) {
      newErrors.email = 'El email es requerido';
    } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
      newErrors.email = 'Introduce un email válido';
    }

    if (!password) {
      newErrors.password = 'La contraseña es requerida';
    } else if (password.length < 6) {
      newErrors.password = 'Mínimo 6 caracteres';
    }

    setErrors(newErrors);
    return Object.keys(newErrors).length === 0;
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!validateForm()) return;
    setIsLoading(true);

    try {
      await new Promise(resolve => setTimeout(resolve, 1500));
      console.log('Login exitoso');
    } catch {
      setErrors({ general: 'Credenciales incorrectas' });
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="w-full max-w-md mx-auto">
      {/* Tarjeta principal */}
      <div className="bg-white rounded-2xl border border-gray-200 p-6 md:p-8 shadow-lg hover:shadow-xl transition-all">
        
        {/* Header */}
        <div className="text-center mb-8">
          <div className="w-16 h-16 mx-auto mb-4 rounded-2xl bg-gradient-to-br from-origen-bosque to-origen-pino flex items-center justify-center shadow-md">
            <User className="w-8 h-8 text-white" />
          </div>
          <h2 className="text-2xl font-bold text-origen-bosque mb-1">Bienvenido</h2>
          <p className="text-sm text-gray-600">Accede a tu cuenta de productor</p>
        </div>

        {/* Error general */}
        <AnimatePresence>
          {errors.general && (
            <motion.div
              initial={{ opacity: 0, y: -10 }}
              animate={{ opacity: 1, y: 0 }}
              exit={{ opacity: 0 }}
              className="mb-6 p-3 bg-red-50 border border-red-200 rounded-lg flex items-center gap-2 text-red-600"
            >
              <AlertCircle className="w-4 h-4 flex-shrink-0" />
              <span className="text-xs">{errors.general}</span>
            </motion.div>
          )}
        </AnimatePresence>

        <form onSubmit={handleSubmit} className="space-y-5">
          
          {/* Campo Email */}
          <div className="space-y-1.5">
            <label className="text-sm font-medium text-origen-bosque flex items-center gap-1.5">
              <Mail className="w-4 h-4 text-origen-pradera" />
              Correo electrónico <span className="text-red-500">*</span>
            </label>
            <div className="relative">
              <input
                type="email"
                value={email}
                onChange={(e) => {
                  setEmail(e.target.value);
                  if (errors.email) setErrors({ ...errors, email: '' });
                }}
                placeholder="nombre@productor.es"
                className={cn(
                  "w-full h-11 px-4 text-sm",
                  "bg-white border rounded-xl",
                  "focus:outline-none focus:ring-2 focus:ring-origen-menta/20",
                  "transition-all duration-200",
                  errors.email
                    ? "border-red-300 focus:border-red-400 focus:ring-red-200"
                    : "border-gray-200 focus:border-origen-menta"
                )}
              />
            </div>
            {/* Mensaje de error en rojo - VERSIÓN INICIAL */}
            {errors.email && (
              <p className="text-xs text-red-500 flex items-center gap-1 mt-1">
                <AlertCircle className="w-3.5 h-3.5" />
                {errors.email}
              </p>
            )}
          </div>

          {/* Campo Contraseña */}
          <div className="space-y-1.5">
            <div className="flex items-center justify-between">
              <label className="text-sm font-medium text-origen-bosque flex items-center gap-1.5">
                <Lock className="w-4 h-4 text-origen-pradera" />
                Contraseña <span className="text-red-500">*</span>
              </label>
              <Link
                href="/recuperar"
                className="text-xs text-origen-pradera hover:text-origen-bosque transition-colors"
              >
                ¿Olvidaste?
              </Link>
            </div>
            <div className="relative">
              <input
                type={showPassword ? "text" : "password"}
                value={password}
                onChange={(e) => {
                  setPassword(e.target.value);
                  if (errors.password) setErrors({ ...errors, password: '' });
                }}
                placeholder="••••••••"
                className={cn(
                  "w-full h-11 px-4 text-sm",
                  "bg-white border rounded-xl",
                  "focus:outline-none focus:ring-2 focus:ring-origen-menta/20",
                  "transition-all duration-200 pr-11",
                  errors.password
                    ? "border-red-300 focus:border-red-400 focus:ring-red-200"
                    : "border-gray-200 focus:border-origen-menta"
                )}
              />
              <button
                type="button"
                onClick={() => setShowPassword(!showPassword)}
                className="absolute right-3 top-1/2 -translate-y-1/2 text-gray-400 hover:text-origen-bosque"
              >
                {showPassword ? <EyeOff className="w-4 h-4" /> : <Eye className="w-4 h-4" />}
              </button>
            </div>
            {/* Mensaje de error en rojo - VERSIÓN INICIAL */}
            {errors.password && (
              <p className="text-xs text-red-500 flex items-center gap-1 mt-1">
                <AlertCircle className="w-3.5 h-3.5" />
                {errors.password}
              </p>
            )}
          </div>

          {/* Checkbox personalizado */}
          <div className="flex items-center gap-2">
            <button
              type="button"
              role="checkbox"
              aria-checked={rememberMe}
              onClick={() => setRememberMe(!rememberMe)}
              className={cn(
                "w-4 h-4 rounded border transition-all",
                "focus:outline-none focus:ring-2 focus:ring-origen-menta/50",
                rememberMe
                  ? "bg-origen-bosque border-origen-bosque"
                  : "border-gray-300 hover:border-origen-pradera"
              )}
            >
              {rememberMe && (
                <Check className="w-4 h-4 text-white" />
              )}
            </button>
            <span className="text-xs text-gray-600">Recordar mi sesión</span>
          </div>

          {/* Botón de acceso */}
          <Button
            type="submit"
            disabled={isLoading}
            className={cn(
              "w-full h-11",
              "bg-origen-bosque hover:bg-origen-pino",
              "text-white text-sm font-semibold",
              "rounded-xl shadow-md hover:shadow-lg",
              "transition-all transform hover:-translate-y-0.5",
              "disabled:opacity-50 disabled:hover:translate-y-0",
              "mt-2"
            )}
          >
            {isLoading ? (
              <div className="flex items-center gap-2">
                <div className="w-4 h-4 border-2 border-white border-t-transparent rounded-full animate-spin" />
                <span>Iniciando sesión...</span>
              </div>
            ) : (
              <span className="flex items-center justify-center gap-2">
                <LogIn className="w-4 h-4" />
                Acceder
              </span>
            )}
          </Button>

          {/* Enlace a registro */}
          <div className="text-center pt-2">
            <p className="text-xs text-gray-500">
              ¿No tienes cuenta?{' '}
              <Link href="/register" className="text-origen-pradera hover:text-origen-bosque font-medium">
                Regístrate
              </Link>
            </p>
          </div>
        </form>
      </div>

      {/* Trust badges */}
      <div className="mt-6 flex flex-wrap items-center justify-center gap-3 text-xs">
        <div className="flex items-center gap-1.5">
          <Shield className="w-3.5 h-3.5 text-origen-pradera" />
          <span className="text-gray-600">SSL 256-bit</span>
        </div>
        <span className="w-1 h-1 rounded-full bg-gray-300" />
        <div className="flex items-center gap-1.5">
          <CheckCircle2 className="w-3.5 h-3.5 text-origen-pradera" />
          <span className="text-gray-600">+500 productores</span>
        </div>
        <span className="w-1 h-1 rounded-full bg-gray-300" />
        <div className="flex items-center gap-1.5">
          <Clock className="w-3.5 h-3.5 text-origen-pradera" />
          <span className="text-gray-600">Respuesta 24h</span>
        </div>
      </div>
    </div>
  );
}